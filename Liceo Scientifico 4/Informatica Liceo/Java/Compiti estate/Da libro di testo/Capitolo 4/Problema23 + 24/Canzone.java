package capitolo4.Problema23;

public class Canzone {

	private String nome;
	private int durataS;
	
	
	
	public Canzone(String nome, int durataS) {
		super();
		this.nome = nome;
		this.durataS = durataS;
	}
	
	
	public String getNome() {
		return nome;
	}
	public void setNome(String nome) {
		this.nome = nome;
	}
	public int getDurataS() {
		return durataS;
	}
	public void setDurataS(int durataS) {
		this.durataS = durataS;
	}
	
	
	
}
