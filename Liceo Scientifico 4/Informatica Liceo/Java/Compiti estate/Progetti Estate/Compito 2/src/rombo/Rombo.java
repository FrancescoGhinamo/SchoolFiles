package rombo;

public class Rombo {
	
	
	private float diagonaleMinore;
	private float diagonaleMaggiore;
	public Rombo(float diagonaleMinore, float diagonaleMaggiore) {
		super();
		this.diagonaleMinore = diagonaleMinore;
		this.diagonaleMaggiore = diagonaleMaggiore;
	}
	public float getDiagonaleMinore() {
		return diagonaleMinore;
	}
	public float getDiagonaleMaggiore() {
		return diagonaleMaggiore;
	}
	
	

}
